int	ft_sqrt(int nb)
{
	int aux;
	int i;

	aux = 0;
	i = 0;
	while (i * i <= nb && i * i >= aux)
	{
		if (i * i == nb)
			return (i);
		aux = i * i;
		i++;
	}
	return (0);
}
